  <h1>Política de Privacidade</h1>

  <h2>1. Introdução</h2>
  <p>
    Bem-vindo ao nosso aplicativo de fretes. Sua privacidade é muito importante para nós. Nesta política, explicamos como coletamos, usamos, protegemos e compartilhamos suas informações pessoais.
  </p>

  <h2>2. Informações que coletamos</h2>
  <ul>
    <li>Nome, foto e dados pessoais fornecidos no cadastro;</li>
    <li>Informações de localização para melhorar a experiência de entrega e logística;</li>
    <li>Dados de contato, como telefone e e-mail;</li>
    <li>Dados técnicos de uso do aplicativo para melhorar a qualidade do serviço.</li>
  </ul>

  <h2>3. Como usamos suas informações</h2>
  <ul>
    <li>Identificar e autenticar você no aplicativo;</li>
    <li>Facilitar o gerenciamento e acompanhamento dos fretes;</li>
    <li>Entrar em contato para suporte e avisos importantes;</li>
    <li>Melhorar nossos serviços e funcionalidades.</li>
  </ul>

  <h2>4. Compartilhamento de dados</h2>
  <p>
    Não vendemos suas informações pessoais a terceiros. Podemos compartilhar dados com parceiros ou prestadores de serviço que nos auxiliam, sempre respeitando a confidencialidade e a segurança.
  </p>

  <h2>5. Segurança dos dados</h2>
  <p>
    Adotamos medidas técnicas e administrativas para proteger suas informações contra acesso não autorizado, alteração, divulgação ou destruição.
  </p>

  <h2>6. Direitos dos usuários</h2>
  <p>
    Você tem direito a acessar, corrigir, atualizar ou solicitar a exclusão dos seus dados pessoais. Para isso, entre em contato conosco pelo e-mail: <a href="mailto:suporte@seudominio.com">suporte@seudominio.com</a>.
  </p>

  <h2>7. Uso de cookies e tecnologias similares</h2>
  <p>
    Nosso aplicativo pode usar tecnologias para melhorar a experiência, como armazenamento local para manter seu login.
  </p>

  <h2>8. Consentimento</h2>
  <p>
    Ao usar nosso aplicativo, você concorda com a coleta e uso dos seus dados conforme descrito nesta política.
  </p>

  <h2>9. Alterações na política de privacidade</h2>
  <p>
